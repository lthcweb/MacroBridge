// ── 程序结构 ──────────────────────────────────────────────────────────────────

void RazerGenerator::visit(ProgramNode& node)
{
    // 提取宏名称（来自顶层 CommentNode 或默认）
    for (auto& child : node.children) {
        if (child->type == AIRNodeType::Comment) {
            auto* c = static_cast<CommentNode*>(child.get());
            // 若注释含 "雷蛇宏:" 前缀，提取宏名
            const std::string prefix = "雷蛇宏: ";
            size_t p = c->text.find(prefix);
            if (p != std::string::npos) {
                size_t end = c->text.find("  (", p);
                m_macroName = c->text.substr(
                    p + prefix.size(),
                    end == std::string::npos
                        ? std::string::npos
                        : end - p - prefix.size());
            }
        }
        child->accept(*this);
    }
}

void RazerGenerator::visit(SequenceNode& node)
{
    for (auto& child : node.children)
        child->accept(*this);
}

void RazerGenerator::visit(HotkeyNode& node)
{
    // 热键体直接展开（雷蛇格式不含触发器定义，触发由硬件决定）
    FlatEvent ev;
    ev.kind = FlatEvent::Kind::Comment;
    ev.text = "热键触发器（雷蛇格式不含触发器，由鼠标硬件决定触发键）";
    pushEvent(ev);

    for (auto& child : node.children)
        child->accept(*this);
}

void RazerGenerator::visit(TriggerNode& node)
{
    FlatEvent ev;
    ev.kind = FlatEvent::Kind::Comment;
    ev.text = "事件触发器（雷蛇格式不含触发器定义）";
    pushEvent(ev);

    for (auto& child : node.children)
        child->accept(*this);
}

void RazerGenerator::visit(FunctionDefNode& node)
{
    pushWarning("函数定义（" + node.name + "）在雷蛇格式中不支持，内容已展开");
    // 尽量展开函数体
    if (!node.children.empty())
        node.children[0]->accept(*this);
}

void RazerGenerator::visit(FunctionCallNode& node)
{
    pushWarning("函数调用（" + node.name + "）在雷蛇格式中不支持");
}

// ── 控制流（雷蛇宏是线性序列，不支持分支/循环，尽量展开）────────────────────

void RazerGenerator::visit(IfNode& node)
{
    pushWarning("if 分支在雷蛇格式中不支持（仅展开 then 分支）");
    if (node.children.size() >= 2)
        node.children[1]->accept(*this); // 展开 then 分支
}

void RazerGenerator::visit(LoopCountNode& node)
{
    // 尝试展开固定次数循环
    int count = 1;
    if (!node.children.empty())
        count = evalIntExpr(node.children[0].get(), 1);

    if (count > 100) {
        pushWarning("循环次数 " + std::to_string(count) +
                    " 过多，已限制为 100 次（雷蛇宏不支持循环，需展开）");
        count = 100;
    } else {
        FlatEvent ev;
        ev.kind = FlatEvent::Kind::Comment;
        ev.text = "Loop " + std::to_string(count) + "次（展开）";
        pushEvent(ev);
    }

    if (node.children.size() >= 2) {
        for (int i = 0; i < count; ++i)
            node.children[1]->accept(*this);
    }
}

