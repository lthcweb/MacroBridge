void RazerGenerator::pushWarning(const std::string& msg, int line)
{
    FlatEvent ev;
    ev.kind = FlatEvent::Kind::Warning;
    ev.text = msg;
    pushEvent(ev);
    m_diags.push_back({ DiagLevel::Warning, line, msg,
                        "该节点在雷蛇格式中无对应表达，已转为注释" });
}

// 从表达式节点提取整数值（支持 ExprNumberNode 和 ExprRandomRangeNode 取中值）
int RazerGenerator::evalIntExpr(AIRNode* node, int defaultVal)
{
    if (!node) return defaultVal;
    if (auto* n = dynamic_cast<ExprNumberNode*>(node))
        return (int)n->value;
    if (auto* n = dynamic_cast<ExprRandomRangeNode*>(node)) {
        // 随机范围取中间值（近似）
        int lo = n->children.size() > 0
                 ? evalIntExpr(n->children[0].get(), 0) : 0;
        int hi = n->children.size() > 1
                 ? evalIntExpr(n->children[1].get(), 0) : 0;
        return (lo + hi) / 2;
    }
    return defaultVal;
}

std::string RazerGenerator::xmlEscape(const std::string& s)
{
    std::string out;
    out.reserve(s.size());
    for (char c : s) {
        switch (c) {
        case '&': out += "&amp;";  break;
        case '<': out += "&lt;";   break;
        case '>': out += "&gt;";   break;
        case '"': out += "&quot;"; break;
        case '\'':out += "&apos;"; break;
        default:  out += c;        break;
        }
    }
    return out;
}

// ============================================================================
//  §2  XML 构建
// ============================================================================

std::string RazerGenerator::buildXml(const std::string& macroName) const
{
    std::ostringstream xml;
    xml << "<?xml version=\"1.0\" encoding=\"utf-8\"?>\n";
    xml << "<!-- Generated by RazerPlugin v1.0.0 -->\n";
    xml << "<Macro "
           "xmlns:xsi=\"http://www.w3.org/2001/XMLSchema-instance\" "
           "xmlns:xsd=\"http://www.w3.org/2001/XMLSchema\">\n";
    xml << "  <Name>" << xmlEscape(macroName) << "</Name>\n";
    xml << "  <Guid>" << m_macroGuid << "</Guid>\n";
    xml << "  <MacroEvents>\n";

    size_t i = 0;
    while (i < m_events.size()) {
        const FlatEvent& ev = m_events[i];

        switch (ev.kind) {

        case FlatEvent::Kind::Comment:
            xml << "    <!-- " << xmlEscape(ev.text) << " -->\n";
            ++i;
            break;

        case FlatEvent::Kind::Warning:
            xml << "    <!-- [WARNING] " << xmlEscape(ev.text) << " -->\n";
            ++i;
            break;

        case FlatEvent::Kind::Sleep:
            xml << "    <MacroEvent>\n";
            xml << "      <Type>6</Type>\n";
            xml << "      <Delay>" << ev.delay << "</Delay>\n";
            xml << "    </MacroEvent>\n";
            ++i;
            break;

        case FlatEvent::Kind::KeyDown:
            xml << "    <MacroEvent>\n";
            xml << "      <Type>1</Type>\n";
            xml << "      <Delay>" << ev.delay << "</Delay>\n";
            xml << "      <KeyboardEvent>\n";
            {
                std::string kn = ev.key != AIRKey::KEY_UNKNOWN
                    ? RazerKeyMap::AIRKeyToRazer(ev.key)
                    : ev.rawKey;
                xml << "        <Key>" << xmlEscape(kn) << "</Key>\n";
            }
            xml << "      </KeyboardEvent>\n";
            xml << "    </MacroEvent>\n";
            ++i;
            break;

        case FlatEvent::Kind::KeyUp:
            xml << "    <MacroEvent>\n";
            xml << "      <Type>2</Type>\n";
            xml << "      <Delay>" << ev.delay << "</Delay>\n";
            xml << "      <KeyboardEvent>\n";
            {
                std::string kn = ev.key != AIRKey::KEY_UNKNOWN
                    ? RazerKeyMap::AIRKeyToRazer(ev.key)
                    : ev.rawKey;
                xml << "        <Key>" << xmlEscape(kn) << "</Key>\n";
            }
            xml << "      </KeyboardEvent>\n";
            xml << "    </MacroEvent>\n";
            ++i;
            break;

        case FlatEvent::Kind::MouseDown:
            xml << "    <MacroEvent>\n";
            xml << "      <Type>4</Type>\n";
            xml << "      <Delay>" << ev.delay << "</Delay>\n";
            xml << "      <MouseButtonEvent>\n";
            xml << "        <Button>"
                << RazerKeyMap::AIRKeyToRazerMouseButton(ev.key)
                << "</Button>\n";
            xml << "      </MouseButtonEvent>\n";
            xml << "    </MacroEvent>\n";
            ++i;
            break;

        case FlatEvent::Kind::MouseUp:
            xml << "    <MacroEvent>\n";
            xml << "      <Type>5</Type>\n";
            xml << "      <Delay>" << ev.delay << "</Delay>\n";
            xml << "      <MouseButtonEvent>\n";
            xml << "        <Button>"
                << RazerKeyMap::AIRKeyToRazerMouseButton(ev.key)
                << "</Button>\n";
            xml << "      </MouseButtonEvent>\n";
            xml << "    </MacroEvent>\n";
            ++i;
            break;

        case FlatEvent::Kind::MouseMove:
            // 连续 MouseMove 事件合并为同一个 Type=3 的 MacroEvent
            xml << "    <MacroEvent>\n";
            xml << "      <Type>3</Type>\n";
            xml << "      <Delay>" << ev.delay << "</Delay>\n";
            xml << "      <MouseMovement>\n";
            while (i < m_events.size() &&
                   m_events[i].kind == FlatEvent::Kind::MouseMove) {
                const FlatEvent& mv = m_events[i];
                xml << "        <MouseMovementEvent>\n";
                xml << "          <Type>3</Type>\n";
                xml << "          <X>" << mv.x << "</X>\n";
                xml << "          <Y>" << mv.y << "</Y>\n";
                if (mv.delay > 0)
                    xml << "          <Delay>" << mv.delay << "</Delay>\n";
                xml << "        </MouseMovementEvent>\n";
                ++i;
            }
            xml << "      </MouseMovement>\n";
            xml << "    </MacroEvent>\n";
            break;
        }
    }

    xml << "  </MacroEvents>\n";
    xml << "  <IsFolder>false</IsFolder>\n";
    xml << "  <FolderGuid>00000000-0000-0000-0000-000000000000</FolderGuid>\n";
    xml << "</Macro>\n";

    return xml.str();
}

