RazerGenerator::RazerGenerator(std::vector<AIRDiagnostic>& diags)
    : m_diags(diags)
{
    m_macroName = "ConvertedMacro";
    m_macroGuid = "00000000-0000-0000-0000-000000000000";
}

std::string RazerGenerator::result() const
{
    return buildXml(m_macroName);
}

