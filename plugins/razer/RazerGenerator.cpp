// ── 时间 ──────────────────────────────────────────────────────────────────────

void RazerGenerator::visit(SleepNode& node)
{
    int ms = 0;
    if (!node.children.empty())
        ms = evalIntExpr(node.children[0].get(), 0);

    FlatEvent ev;
    ev.kind  = FlatEvent::Kind::Sleep;
    ev.delay = ms;
    pushEvent(ev);
}

